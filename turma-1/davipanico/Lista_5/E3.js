/* Faça um programa que escreva na tela os números pares entre 0 e 50, usando um
comando For. Não utilize nenhum IF/THEN neste programa, apenas o comando For-Do. 
*/

// Condição, aqui declaramos a variável 'i' com o valor de 0, logo após dizemos que enquanto a variável 'i' for menor ou igual (<=)
// que 50, então incremente + 2 (i = i + 2) enquanto imprimir a variável 'i' no console.
for (i = 0; i <= 50; i = i + 2) {
    console.log(i)
}